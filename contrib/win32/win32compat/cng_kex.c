DH *
cng_dh_new_group_asc(const char *gen, const char *modulus)
{

	DH *dh;
	BIGNUM * p = NULL;
	BIGNUM * g = NULL;

	if (BN_hex2bn(&p, modulus) == 0 ||
		BN_hex2bn(&g, gen) == 0) {
		return NULL;
	}
	dh = cng_dh_new_group(g, p);
	BN_free(p);
	BN_free(g);
	return dh;

}



